#!/bin/bash
source func.sh
#-----------------------------------------------------------------------------------------------------------------------------#
#-------------------------------------------------------BEGINNING OF CODE-----------------------------------------------------#

if [ $# -eq   3 ] && [ $1 -ge 0 ] && [ $3 -ge 0 ] 
then
 
case $2 in
 "+")

	sum=`echo "$1+$3"|bc`
	echo sum is $sum;;
 "-" )
	sub=`echo "$1-$3"|bc`
	echo sub is $sub;;
 "x" )

	mul=`echo "$1*$3"|bc`
	echo mul is $mul;;

 "%" )
	rem=`echo "$1%$3"|bc`
	echo rem is $rem;;

 "/" )
	div=`echo "$1/$3"|bc`
	echo div is $div;;
*)
	echo invalid operator;;
esac

else
     echo "Invalid input"
fi

cal arcal.sh 



