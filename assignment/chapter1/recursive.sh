# function for recursion

function recurse()
{ 
	echo "$1"
	arr=("$@")		
	arr1=()
	for((i=1; i<=${#arr[@]}; i++ ))
	do
		arr1[i-1]=${arr[$i]}
	
	done
	if [ $1 ]
	then
	recurse ${arr1[@]}
	fi
}

#calling the recurse function
recurse $@


