#Program to read n and generate pattern1

#!/bin/bash

#-----------------------------------------------------------------------------------------------------------------------------#
#-------------------------------------------------------BEGINNING OF CODE-----------------------------------------------------#


if [ "$#" -eq 0 ]
then
	echo "invalid input"
else
	array=("$@")
	for (( i=0; i<$#; i++ ))
	do
		echo "${array[i]}- ${#array[i]}"
	done
fi


#----------------------------------------------------------END OF CODE--------------------------------------------------------#
