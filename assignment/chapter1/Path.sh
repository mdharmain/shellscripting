#Script to display the executable files in each directory of $path

#!/bin/bash

#-----------------------------------------------------------------------------------------------------------------------------#
#-------------------------------------------------------BEGINNING OF CODE-----------------------------------------------------#

arr=(`echo  $PATH  | tr ":" "\n"` )
echo ${arr[@]}
Tcount=0
for ((i=0; i<${#arr[@]}; i++ ))
do
	Tcount=$(($count+$Tcount))
	echo "DIRECTORY: ${arr[i]}"
	count=`find ${arr[i]} -executable | wc -c `
	echo "$count"
	
done
    echo "Total count : $Tcount"

















#----------------------------------------------------------END OF CODE--------------------------------------------------------#
